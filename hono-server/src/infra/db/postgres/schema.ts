  POSTGRES_CREATE_TOKEN_OTPS_TABLE,
  POSTGRES_CREATE_OUTBOX_EVENTS_TABLE,
  POSTGRES_CREATE_API_KEYS_TABLE,
  POSTGRES_ALTER_API_KEYS_ADD_KEY_VAL,
